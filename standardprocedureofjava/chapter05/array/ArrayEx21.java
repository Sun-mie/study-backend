public class ArrayEx21 {
    public static void main(String[] args) {
        char[][] chArr = {
                {'A', 'B', 'C', 'D', 'E'},
                {'F', 'G', 'H', 'I'}
        };
        for(int i=0; i<chArr.length; i++){
            System.out.println(chArr[i]);
        }
    }
}
